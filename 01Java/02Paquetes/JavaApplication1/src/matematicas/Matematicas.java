public class Matematicas {
    
    public static float suma(float a, float b){
            return a+b;
    }
    public static float resta(float a, float b){
            return a-b;
    }
    public static float multiplicacion(float a, float b){
            return a*b;
    }
    public static float division(float a, float b){
            return a/b;
    }
}
